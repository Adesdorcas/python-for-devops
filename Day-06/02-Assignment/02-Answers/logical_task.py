# Define boolean variables
x = True
y = False

# Perform logical operations
and_result = x and y
or_result = x or y
not_result_x = not x
not_result_y = not y

# Print the results
print("Result of x and y:", and_result)
print("Result of x or y:", or_result)
print("Result of not x:", not_result_x)
print("Result of not y:", not_result_y)